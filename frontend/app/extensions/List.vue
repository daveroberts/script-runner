<template>
  <div>
    <h1>Extension Methods</h1>
    <div v-if="!extensions">
      Loading...
    </div>
    <div v-else>
      <div v-for="ext in extensions">
        <h2><a href="#" class="extension_link" @click.prevent="toggle_ext(ext.name)">{{ext.name}}</a></h2>
        <div v-if="show_methods(ext.name)">
          <ul class="method_list">
            <li class="method" v-for="method in ext.methods">{{method}}</li>
          </ul>
        </div>
      </div>
    </div>
  </div>
</template>
<script>
import state from '../state/state.js'
import * as senate from '../state'
import initial from '../state/initial.js'
export default {
  data: function(){
    return {
      state: state,
      toggles: []
    }
  },
  computed: {
    extensions(){ return state.extensions },
  },
  created: function(){
    console.log("About to fetch")
    fetch(`/api/extensions`).then((res)=>{
      if (res.ok){ return res.json() }
    }).then(extensions=>{
      state.extensions = extensions
    }).catch((err)=>{
      console.log(err)
    })
  },
  methods: {
    toggle_ext(name){
      var idx = this.toggles.indexOf(name)
      if (idx == -1){
        this.toggles.push(name)
      } else {
        this.toggles.splice(idx, 1)
      }
    },
    show_methods(name){
      var idx = this.toggles.indexOf(name)
      return idx != -1
    }
  }
}
</script>
